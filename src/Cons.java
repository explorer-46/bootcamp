public class Cons {
    int dosa;
    int coffee;
    int tea;

    public Cons(int dosa, int coffee, int tea) {
        this.dosa = dosa;
        this.coffee = coffee;
        this.tea = tea;
    }

    public int getDosa() {
        return dosa;
    }

    public void setDosa(int dosa) {
        this.dosa = dosa;
    }

    public int getCoffee() {
        return coffee;
    }

    public void setCoffee(int coffee) {
        this.coffee = coffee;
    }

    public int getTea() {
        return tea;
    }

    public void setTea(int tea) {
        this.tea = tea;
    }
}
