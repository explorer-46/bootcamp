public class Setter {
    Integer Tea;
    Integer Coffee;
    Integer Icecream;

    public Setter(Integer tea, Integer coffee, Integer icecream) {
        Tea = tea;
        Coffee = coffee;
        Icecream = icecream;
    }

    public Integer getTea() {
        return Tea;
    }

    public void setTea(Integer tea) {
        Tea = tea;
    }

    public Integer getCoffee() {
        return Coffee;
    }

    public void setCoffee(Integer coffee) {
        Coffee = coffee;
    }

    public Integer getIcecream() {
        return Icecream;
    }

    public void setIcecream(Integer icecream) {
        Icecream = icecream;
    }
}
